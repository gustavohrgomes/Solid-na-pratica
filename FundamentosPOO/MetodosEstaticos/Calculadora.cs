using System;
using System.Collections.Generic;
using System.Text;

namespace MetodosEstaticos
{
    class Calculadora
    {
        public static int Somar(int num1, int num2)
        {
            return num1 + num2;
        }
        public static int Subtrair(int num1, int num2)
        {
            return num1 - num2;
        }
    }
}
